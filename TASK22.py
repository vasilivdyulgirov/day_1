# Примери с ord()
print(ord('a'))   # 97
print(ord('A'))   # 65
print(ord('0'))   # 48
print(ord('8'))   # 56

# Аритметика със символи
print(ord('a') + 4)  # 97 + 4 = 101

# Преобразуване от символ към число
print(ord('8') - ord('0'))  # 56 - 48 = 8

# Обратно — от код към символ
print(chr(65))   # 'A'
print(chr(97))   # 'a'
