print(True or True)     # True
print(True or False)    # True
print(False or True)    # True
print(False or False)   # False

a = True
b = False

print("a and b =", a and b)   # False
print("a or b =", a or b)     # True
print("not a =", not a)       # False
print("not b =", not b)       # True
