# Програма за изчисляване на лице и обиколка на окръжност

PI = 3.14

# Въвеждане на радиуса
r = float(input("Въведете радиус r = "))

# Изчисления
S = PI * r * r          # Лице на кръга
P = 2 * PI * r          # Обиколка на окръжността

# Извеждане на резултатите
print(f"S = {S}")
print(f"P = {P}")