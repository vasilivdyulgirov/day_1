import math  # За да използваме sqrt (корен квадратен)

# Въвеждане на стойности за a, b и c
a = float(input("Въведете основа a: "))
b = float(input("Въведете основа b: "))
c = float(input("Въведете бедро c: "))

# Стъпка 1: пресмятане на d
d = (a - b) / 2

# Стъпка 2: пресмятане на h по Питагоровата теорема
h = math.sqrt(c**2 - d**2)

# Стъпка 3: пресмятане на лицето на трапеца
S = ((a + b) / 2) * h

# Извеждане на резултата
print(f"Лицето на трапеца е: {S}")
