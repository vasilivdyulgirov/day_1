"""
=========================================
Урок: Работа със символи и ASCII кодове
=========================================
"""


"""
Част 1: ASCII кодове на цифрите
---------------------------------
Всеки символ има числов код в таблицата ASCII.
Например:
'0' → 48
'1' → 49
...
'9' → 57
"""
print("Част 1: ASCII кодове на цифрите")
print("Символ '8' има код:", ord('8'))  # 56
print("Символ '0' има код:", ord('0'))  # 48
print()


"""
Част 2: Защо се събира и изважда при работа със символи?
-----------------------------------------------------------
Символите в компютъра всъщност са числа (ASCII кодове).
Когато пишем '8' - '0', това означава:

1. '8' → код 56
2. '0' → код 48
3. 56 - 48 = 8 → това е числото 8

Тоест, изваждаме кода на '0' за да "нулираме" кода на символа
и да получим неговата цифрова стойност.

Същото важи и за събиране:
Ако имаме число 5 и искаме символ '5':
chr(5 + ord('0')) → 53 → '5'
"""
print("Част 2: Защо се събира и изважда при работа със символи")
print("'8' - '0' =", ord('8') - ord('0'))  # 56 - 48 = 8
print("Число 5 → символ:", chr(5 + ord('0')))
print()


"""
Част 3: Преобразуване от символ към число
------------------------------------------
Формула:
число = ord(символ) - ord('0')
"""
print("Част 3: Преобразуване от символ към число")
cifra = '5'
chislo = ord(cifra) - ord('0')
print("Символ:", cifra, "→ Число:", chislo)
print()


"""
Част 4: Преобразуване от число към символ
------------------------------------------
Формула:
символ = chr(число + ord('0'))
"""
print("Част 4: Преобразуване от число към символ")
chislo = 7
cifra = chr(chislo + ord('0'))
print("Число:", chislo, "→ Символ:", cifra)
print()


"""
Част 5: Пълна таблица — цифри и кодове
----------------------------------------
"""
print("Част 5: Таблица с ASCII кодовете на цифрите")
for i in range(10):
    cifra = chr(i + ord('0'))
    kod = ord(cifra)
    print(f"'{cifra}' → ASCII {kod} → число {i}")
print()


"""
Част 6: Аритметични операции със символи
------------------------------------------
Работи върху техните ASCII кодове.
Пример:
'a' → 97 → 97 + 4 = 101
"""
print("Част 6: Аритметични операции със символи")
print("'a' + 4 =", ord('a') + 4)   # 97 + 4 = 101
print("'3' + '2' =", ord('3') + ord('2'))  # 51 + 50 = 101
print("'8' - '0' =", ord('8') - ord('0'))  # 56 - 48 = 8
print()


"""
Част 7: Проверка — обратно преобразуване
------------------------------------------
"""
print("Част 7: Проверка")
k = ord('5')
print("Код на '5' е:", k)
simvol = chr(k)
print("Символ от код", k, "е:", simvol)
print()
