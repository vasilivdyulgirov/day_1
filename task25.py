"""
Пример 1:
(int)3.8 в C++ → преобразуване от дробно към цяло число.
В Python става с int().
"""
print(int(3.8))   # Резултат: 3


"""
Пример 2:
(char)42 в C++ → преобразуване от цяло число към символ по ASCII.
В Python става с chr().
"""
print(chr(42))    # Резултат: * (42 е кодът на '*')


"""
Пример 3:
(double)10/4 в C++ → първо 10 става 10.0, после се дели → 2.5
В Python става с float(10)/4.
"""
print(float(10) / 4)   # Резултат: 2.5


"""
Пример 4:
(double)(10/4) в C++ → първо се изпълнява 10/4 като цяло деление (→ 2),
после резултатът се преобразува в дробно → 2.0.
В Python използваме целочислено деление // и после float().
"""
print(float(10 // 4))  # Резултат: 2.0
