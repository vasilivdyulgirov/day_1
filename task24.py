"""
В C++ има оператори ++ и -- за увеличаване или намаляване с 1.
Те могат да се използват по два начина:

- Префиксно (++a или --a) → първо се променя стойността, после се използва.
- Постфиксно (a++ или a--) → първо се използва старата стойност, после се променя.

В Python НЯМА ++ и --. Използват се:
- a += 1 (увеличи с 1)
- a -= 1 (намали с 1)

В Python няма разлика кога се изпълнява — винаги първо се променя, после се използва.
"""

a = 20
b = 15

# Префиксно ++a в C++ → в Python:
a += 1
print("a =", a)  # извежда 21

print("a =", a)  # извежда 21 (стойността вече е увеличена)

# Постфиксно b++ в C++ → в Python:
print("b =", b)  # извежда 15 (старата стойност)
b += 1           # b става 16

print("b =", b)  # извежда 16

# Префиксно --a в C++ → в Python:
a -= 1
print("a =", a)  # извежда 20

print("a =", a)  # извежда 20

# Постфиксно b-- в C++ → в Python:
print("b =", b)  # извежда 16 (старата стойност)
b -= 1           # b става 15

print("b =", b)  # извежда 15
